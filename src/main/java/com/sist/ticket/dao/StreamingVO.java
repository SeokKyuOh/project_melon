package com.sist.ticket.dao;

public class StreamingVO {
	private int streaming_id;
	private String streaming_name;
	private int streaming_period;
	private int streaming_price;

	public int getStreaming_id() {
		return streaming_id;
	}

	public void setStreaming_id(int streaming_id) {
		this.streaming_id = streaming_id;
	}

	public String getStreaming_name() {
		return streaming_name;
	}

	public void setStreaming_name(String streaming_name) {
		this.streaming_name = streaming_name;
	}

	public int getStreaming_period() {
		return streaming_period;
	}

	public void setStreaming_period(int streaming_period) {
		this.streaming_period = streaming_period;
	}

	public int getStreaming_price() {
		return streaming_price;
	}

	public void setStreaming_price(int streaming_price) {
		this.streaming_price = streaming_price;
	}

}
